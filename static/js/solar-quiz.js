const quizData = [
    {
      question: "What is the average cost of a kWh once solar panels are installed?",
      options: ["0¢", "1-2¢", "3-5¢", "6-8¢"],
      answer: "6-8¢"
    },
    {
      question: "How much maintenance do solar panels require?",
      options: ["A lot", "A little", "None", "Only when it rains"],
      answer: "A little"
    },
    {
      question: "In 2021, how much of the world's electricity was generated by solar power?",
      options: ["4%", "10%", "20%", "99%"],
      answer: "4%"
    }
  ];
  
  const questionElement = document.getElementById("question");
  const optionsElement = document.getElementById("options");
  const submitButton = document.getElementById("submit");
  
  let currentQuestion = 0;
  let score = 0;
  
  function showQuestion() {
    selectAnswerRunning = false;

    const question = quizData[currentQuestion];
    questionElement.innerText = question.question;
  
    optionsElement.innerHTML = "";
    question.options.forEach(option => {
      const button = document.createElement("button");
      button.innerText = option;
      optionsElement.appendChild(button);
      button.addEventListener("click", selectAnswer);
    });
  }
  
  var selectAnswerRunning = false;

  function selectAnswer(e) {

    if (selectAnswerRunning) {
      return;
    }

    selectAnswerRunning = true;

    const selectedButton = e.target;
    const answer = quizData[currentQuestion].answer;
  
    if (selectedButton.innerText === answer) {
      // set the button color to green
      selectedButton.style.backgroundColor = "green";
      score++;
    } else {
      // set the button color to red
      selectedButton.style.backgroundColor = "red";
    }
  
    currentQuestion++;
  
    if (currentQuestion < quizData.length) {
      setTimeout(showQuestion, 1000);
    } else {
      setTimeout(showResult, 1000);
    }
  }
  
